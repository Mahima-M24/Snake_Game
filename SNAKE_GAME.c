    int choice;
    clear_screen();
    print_title();
    set_color(0x0B);
    printf("  ***** Select Speed *****\n\n");
    set_color(0x07);
    printf("  [1]  Slow    (%d ms/step)\n", SPEED_SLOW);
    printf("  [2]  Medium  (%d ms/step)\n", SPEED_MEDIUM);
