/*    Main game loop */
static void run_game(void) {
    DWORD last, now;

    g_map_id   = select_map();
    g_speed_ms = select_speed();
    init_game();
    full_redraw();

    last = GetTickCount();

    while (g_running) {
        if (handle_input()) break;       /* ESC */

        now = GetTickCount();
        if ((DWORD)(now - last) >= (DWORD)g_speed_ms) {
            if (update_snake())          /* collision = dead */
                g_running = 0;
            last = now;
        }
        Sleep(5);                        /* yield CPU */
    }

    check_and_save_score(g_score);
}

/*    Main menu  */
static void main_menu(void) {
    int choice;
    for (;;) {
        show_cursor();
        clear_screen();
        print_title();
        set_color(0x0B);
        printf("  ***** Main Menu *****\n\n");
        set_color(0x07);
        printf("  [1]  Play\n");
        printf("  [2]  High Scores\n");
        printf("  [3]  Controls / Help\n");
        printf("  [4]  Quit\n");
        printf("\n  Enter choice: ");
        fflush(stdout);

        if (scanf("%d", &choice) != 1) choice = 0;
        while (_kbhit()) _getch();

        switch (choice) {
            case 1: run_game();         break;
            case 2: show_high_scores(); break;
            case 3: show_controls();    break;
            case 4:
                clear_screen();
                set_color(0x0A);
                printf("\n  Thanks for playing!  Goodbye.\n\n");
                set_color(0x07);
                show_cursor();
                return;
            default: break;
        }
    }
}

