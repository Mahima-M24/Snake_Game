/*    Console helpers    */
static HANDLE hOut;

static void console_init(void) {
    COORD       bufSize  = { (SHORT)CON_COLS, (SHORT)(CON_ROWS + 2) };
    SMALL_RECT  winRect  = { 0, 0, (SHORT)(CON_COLS - 1), (SHORT)(CON_ROWS - 1) };
    hOut = GetStdHandle(STD_OUTPUT_HANDLE);
    /* Enlarge buffer before shrinking the window, or vice-versa as needed */
    SetConsoleScreenBufferSize(hOut, bufSize);
    SetConsoleWindowInfo(hOut, TRUE, &winRect);
    SetConsoleTitle("Snake Game");
}

static void gotoxy(int x, int y) {
    COORD c = { (SHORT)x, (SHORT)y };
    SetConsoleCursorPosition(hOut, c);
}

static void hide_cursor(void) {
    CONSOLE_CURSOR_INFO ci = { 1, FALSE };
    SetConsoleCursorInfo(hOut, &ci);
}

static void show_cursor(void) {
    CONSOLE_CURSOR_INFO ci = { 100, TRUE };
    SetConsoleCursorInfo(hOut, &ci);
}

static void set_color(int attr) {
    SetConsoleTextAttribute(hOut, (WORD)attr);
}

static void clear_screen(void) {
    COORD    origin = { 0, 0 };
    DWORD    written;
    CONSOLE_SCREEN_BUFFER_INFO csbi;
    GetConsoleScreenBufferInfo(hOut, &csbi);
    DWORD cells = (DWORD)csbi.dwSize.X * (DWORD)csbi.dwSize.Y;
    FillConsoleOutputCharacter(hOut,  ' ',                  cells, origin, &written);
    FillConsoleOutputAttribute(hOut,  csbi.wAttributes,     cells, origin, &written);
    SetConsoleCursorPosition(hOut, origin);
}

/* Drawing */
static void draw_border(void) {
    int i;
    set_color(0x0B); /* bright cyan */
    gotoxy(0, 0);
    putchar('+');
    for (i = 0; i < MAP_W; i++) putchar('-');
    putchar('+');
    gotoxy(0, MAP_H + 1);
    putchar('+');
    for (i = 0; i < MAP_W; i++) putchar('-');
    putchar('+');
    for (i = 1; i <= MAP_H; i++) {
        gotoxy(0,         i); putchar('|');
        gotoxy(MAP_W + 1, i); putchar('|');
    }
    set_color(0x07);
}

static void draw_map_walls(void) {
    int x, y;
    set_color(0x08); /* dark grey */
    for (y = 0; y < MAP_H; y++)
        for (x = 0; x < MAP_W; x++)
            if (g_map[y * (MAP_W + 1) + x] == '#') {
                gotoxy(SCREEN_X(x), SCREEN_Y(y));
                putchar('#');
            }
    set_color(0x07);
}

static void draw_food(void) {
    set_color(0x0C); /* bright red */
    gotoxy(SCREEN_X(g_food.x), SCREEN_Y(g_food.y));
    putchar('$');
    set_color(0x07);
}

static void draw_snake(void) {
    int i;
    set_color(0x0A); /* bright green — head */
    gotoxy(SCREEN_X(g_snake.body[0].x), SCREEN_Y(g_snake.body[0].y));
    putchar('@');
    set_color(0x02); /* dark green — body */
    for (i = 1; i < g_snake.length; i++) {
        gotoxy(SCREEN_X(g_snake.body[i].x), SCREEN_Y(g_snake.body[i].y));
        putchar('o');
    }
    set_color(0x07);
}

static void erase_cell(int x, int y) {
    gotoxy(SCREEN_X(x), SCREEN_Y(y));
    putchar(' ');
}

static void draw_status(void) {
    const char *spd =
        (g_speed_ms == SPEED_SLOW)   ? "Slow"   :
        (g_speed_ms == SPEED_MEDIUM) ? "Medium" :
        (g_speed_ms == SPEED_FAST)   ? "Fast"   : "Insane";
    gotoxy(0, MAP_H + 2);
    set_color(0x0E);
    printf("  Score: %-6d  Map: %-34s  Speed: %-7s  [ESC] Quit  ",
           g_score, MAP_NAMES[g_map_id], spd);
    set_color(0x07);
}

